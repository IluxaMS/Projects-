void print_visual_char(unsigned char c, FILE *out) { // эта функция реализует флаг -v (visual). Её задача — превратить невидимые управляющие символы (коды 0-31, 127, >127) в читаемый текст (например, ^C, M-X). unsigned char (беззнаковый): Все 8 бит идут под значение числа. Знака нет (всегда положительное). Диапазон: от 0 до 255.
  if (c == '\t') { // обработка табуляции (\t)
    fprintf(out, "^I"); // в нотации Unix Control-символы обозначаются как ^ + буква. Tab — это Control+I. fprintf (File PRINTF) позволяет выбрать, КУДА писать. Первым аргументом мы передаем поток.
  } else if (c == '\n') { // обработка переноса строки (\n)
    fputc('\n', out); // просто печатаем его как есть. Если что fputc - это быстрая функция для вывода одного символа.
  } else if (c < 32 || c == 127) { // 
    fprintf(out, "^%c", c + 64); // %c: Спецификатор формата - означает «вставь сюда следующий аргумент как символ», чтобы вместо числа получилась буква  
  } else if (c > 127) {
    fprintf(out, "M-%c", c - 128 + 64);
  } else {
    fputc(c, out);
  }
} // 

void parse_cat_flags(int argc, char *argv[], CatFlags *flags) { // Эта функция читает аргументы командной строки и заполняет структуру flags.
  memset(flags, 0, sizeof(CatFlags)); // с помощью memset мы заполняем всю память структуры(CatFlags) нулями, так как флаги могут содержать случайный мусор (единицы), и программа вела бы себя непредсказуемо, а sizeof(CatFlags) гарантирует, что мы обнулим ровно столько байт, сколько занимает структура
  for (int i = 1; i < argc; i++) { // внешний цикл (i): Перебирает все аргументы, начиная с 1 (пропуская имя программы).
    if (argv[i][0] == '-') { // проверка argv[i][0] == '-': Если аргумент начинается с минуса — это флаг. Если нет - пропускаем его
      for (int j = 1; argv[i][j] != '\0'; j++) { // внутренний цикл (j): Перебирает символы внутри одного аргумента(это позволяет   поддерживать слитные флаги(например "-ne")), начинаем со второго символа, так как первый — это минус
        switch (argv[i][j]) {
          case 'b': // нумерует только непустые строки
            flags->b = true; // просто включаем флаг. Ничего больше не требуется.
            break;
          case 'e': // показывает $ в конце строки + включает -v. Важный нюанс ТЗ: Флаг -e подразумевает включение флага -v.
            flags->e = true; // поэтому мы ставим true в оба поля. Это нужно, чтобы при выводе программа знала: нужно показывать не только $ (логика -e), но и другие непечатные символы (логика -v).
            flags->v = true;
            break;
          case 'E': // отличие от -e: -E показывает $, но НЕ включает автоматически -v.
            flags->E_flag = true; // нужно, чтобы в функции вывода мы поняли: «Ага, пользователь хотел только $, не надо включать полную визуализацию».
            flags->e = true; // тут собственно просто включаем флаг
            break;
          case 'n': // нумерует все строки.
            flags->n = true; // просто включаем флаг. Ничего больше не требуется.
            break;
          case 's': // сжимает множественные пустые строки в одну.
            flags->s = true; // просто включаем флаг. Ничего больше не требуется.
            break;
          case 't': // -t тоже подразумевает -v. -t: Показывать табы как ^I и визуализировать остальное (v=true).
            flags->t = true; // поэтому мы ставим true в оба поля.
            flags->v = true;
            break;
          case 'T': // -T не подразумевает -v. Показывать табы как ^I, но остальное не трогать (v=false).
            flags->T_flag = true; // и говорим функции вывода, что мы используем -T
            flags->t = true; // поэтому просто включаем флаг
            break;  
          case 'v': // визуализирует непечатные символы (^X, M-X).
            flags->v = true; // просто включаем флаг. Ничего больше не требуется.
            break;
          default: // на случай, если попадется неизвестный символ после минуса
            break;
        }
      }
    }
  }
}

static void handle_flag_e(char **argv, int *i, int argc, char **patterns, // char **argv: Доступ ко всем словам команды. int *i: Индекс текущего слова. int argc: Граница массива
                          int *count, bool *e_flag, bool *needs_free) { // int *count: Сколько уже нашли паттернов. bool *e_flag: Флаг «паттерн есть». bool *needs_free: Нужно ли удалять память?
  if (*i + 1 < argc && *count < MAX_PATTERNS) { // Главный цикл в parse_grep_flags_main дошел до индекса i = 1 (аргумент "-e"). Он видит букву 'e' и вызывает нашу функцию handle_flag_e - *i (значение i) = 1. 1 + 1 = 2. 2 < 4 (argc)? Да. (Следующий аргумент существует) и (&&) *count = 0. 0 < 100? Да.
    (*i)++; // Было i(который в parse_grep_flags_main) = 1. Стало i = 2. Зачем: Теперь главный индекс в main «перепрыгнул» через слово "warning", а ниже(в patterns[(*count)] = argv[*i];) у нас будет сохранение паттерна как номер индекса. Когда цикл в main продолжится, он начнет со следующего аргумента (log.txt), а не будет пытаться читать "warning" как новый флаг.
    patterns[(*count)] = argv[*i]; // собственно самое сохраннение паттерна - patterns[0] = argv[2], а argv[2], например в строке "./s21_grep -e "warning" log.txt", это "warning", то бишь нужный нам паттерн, поэтому мы его записываем как первый паттерн
    needs_free[*count] = false; // флаг памяти. Зачем: Мы сказали системе: «Паттерн в ячейке 0 лежит в памяти программы (argv), удалять его НЕ нужно». Роль needs_free = true: Сказать циклу очистки: «Этот элемент был создан через malloc, обязательно вызови для него free()».
    (*count)++; // просто увеличиваем счётчик флагов, так как нашли один паттерн
    *e_flag = true; // просто включаем флаг e
  }
}

static void handle_flag_f(char **argv, int *i, int argc, GrepFlags *flags,
                          int *count) {
  if (*i + 1 < argc) { // 1+1=2 < 4. ОК.
    (*i)++; // i стало 2. Теперь argv[2] это "patterns.txt", но так как "patterns.txt" содержит в себе несколько паттернов, то нам собстенно нужно открыть и прочитать этот файл, чтобы узнать какие паттерны есть
    FILE *fp = fopen(argv[*i], "r"); // fopen("patterns.txt", "r"). Создаём переменную указатель(*fp) с литералом FILE и с помощью функции fopen мы пытаемся прочитать данный файл
    if (fp) {
      char buf[1024];
      while (fgets(buf, sizeof(buf), fp) && *count < MAX_PATTERNS) { // Читает первую строку: buf = "error\n" и(&&) также условие, что паттерны который мы читаем не выходит за пределы MAX_PATTERNS
        buf[strcspn(buf, "\n")] = 0; // очистка строки - находит \n и заменяет на \0(0 - равносильно \0 — концу строки), чтобы buf вместо "error\n" стал просто "error", так как с паттерном "error\n" мы будем искать папки не с именем "error", а с именем "error\n"
        if (strlen(buf) > 0) { // Сохранение (Динамическая память!), мы проверяем, не пустая ли строка - функция strlen (String Length) возвращает длину строки (количество символов до нулевого терминатора \0).
          flags->patterns[*count] = strdup(buf); // flags->patterns[0] = Адрес новой копии строки "error". strdup: Вычисляет длину исходной строки, Выделяет ровно столько памяти через malloc (+1 байт под \0), Копирует туда содержимое строки, Возвращает указатель на новую копию. Зачем: Чтобы создать независимую копию строки, которая не исчезнет и не изменится, когда исходный буфер (buf) будет перезаписан следующей строкой из файла, то есть если бы мы написали = buf, то на следующей итерации buf изменился бы, и первый паттерн тоже «превратился» бы во вторую строку. strdup спасает нас от этого.
/*           Разбор всей строки:
flags->patterns[*count] = strdup(buf);
Берем структуру по указателю flags.
Находим в ней массив patterns.
Выбираем ячейку с индексом *count (текущий свободный слот).
Создаем копию строки из buf.
Записываем адрес этой копии в ячейку массива. 

Так же запись flags->patterns равносильная (*flags).patterns, это просто синтаксический сахар*/
          flags->pattern_needs_free[*count] = true; // flags->pattern_needs_free[0] = true. Зачем: Мы сказали: «Паттерн в ячейке 0 создан через strdup, его нужно будет удалить через free() в конце программы!», происходит это в конце main в s21_grep.c в последнем цикле:
          (*count)++; // count стал 1
          flags->e = true; // Паттерн задан
          flags->f = true; // Использован флаг -f
        }
      }
      fclose(fp); // Закрытие
    } else if (!flags->s) { // Флаг -s (silent) означает «молчать об ошибках», то есть если пользователь ввел -s, мы не должны спамить ошибками в консоль, если файл не найден.
      fprintf(stderr, "s21_grep: %s: %s\n", argv[*i], strerror(errno)); // errno - это глобальная переменная, в которую система записала код последней ошибки (например, код 2 = «файл не найден»), а strerror(...) превращает этот числовой код в понятный текст на английском (например, "No such file or directory").
    }
  }
}

// Итог: Функция handle_flag_e открыла файл, прочитала 3 строки, создала 3 независимые копии строк в памяти, пометила их для удаления и увеличила счетчик паттернов на 3.

void parse_grep_flags_main(int argc, char *argv[], GrepFlags *flags,
                           int *pattern_count, int *file_start_idx) {
  memset(flags, 0, sizeof(GrepFlags)); // обнуление всех флагов
  *pattern_count = 0; // счётчик паттернов
  *file_start_idx = argc;

  for (int k = 0; k < MAX_PATTERNS; k++) { // Мы заранее помечаем все ячейки как «не требующие удаления». Функция handle_flag_f позже изменит нужные ячейки на true. Это защита от случайного освобождения мусорной памяти
    flags->pattern_needs_free[k] = false;
  }

  for (int i = 1; i < argc; i++) {
    bool skip_rest = false; // Это элегантная замена goto. Когда мы «съедаем» следующий аргумент как значение флага (для -e или -f), мы должны немедленно прекратить перебор символов текущего аргумента, чтобы не интерпретировать значение как новые флаги.
    if (argv[i][0] == '-') {
      for (int j = 1; argv[i][j] != '\0' && !skip_rest; j++) {
        switch (argv[i][j]) {
          case 'e': // задает шаблон поиска явно. Позволяет иметь несколько шаблонов или искать строку, начинающуюся с -
            handle_flag_e(argv, &i, argc, flags->patterns, pattern_count,
                          &flags->e, flags->pattern_needs_free);
            skip_rest = true;
            break;
          case 'f': // читает список паттернов из указанного файла (каждая строка — новый паттерн)
            handle_flag_f(argv, &i, argc, flags, pattern_count);
            skip_rest = true;
            break;
          case 'i': // игнорирует регистр букв
            flags->i = true;
            break;
          case 'v': // инвертирует поиск. Выводит строки, которые НЕ соответствуют паттерну
            flags->v = true;
            break;
          case 'c': // не выводит строки, а только количество совпадений
            flags->c = true;
            break;
          case 'l': // выводит только имена файлов, где есть совпадения (по одному разу на файл)
            flags->l = true;
            break;
          case 'n': // добавляет номер строки перед каждым совпадением
            flags->n = true;
            break;
          case 'h': // подавляет вывод имени файла при поиске в нескольких файлах
            flags->h = true;
            break;
          case 's':
            flags->s = true; // подавляет сообщения об ошибках (например, если файл не найден)
            break;
          case 'o': // выводит только саму совпавшую часть строки, а не всю строку целиком. Каждое совпадение с новой строки
            flags->o = true;
            break;
          default:
            break;
        }
      }
    } else {
      if (!flags->e && *pattern_count < MAX_PATTERNS) {
        // Случай А: Паттерн еще не задан. Значит, это ОН.
        flags->patterns[*pattern_count] = argv[i];
        flags->pattern_needs_free[*pattern_count] = false;
        (*pattern_count)++; // Помечаем, что паттерн теперь есть
        flags->e = true;
      } else if (flags->e && *file_start_idx == argc) {
        // Случай Б: Паттерн уже есть. Значит, это ПЕРВЫЙ файл.
        *file_start_idx = i;
      }
        // Если файлы уже шли, мы просто ничего не делаем здесь.
        // Они будут обработаны позже циклом, использующим file_start_idx.
    }
  }
}

static bool is_line_empty(const char *line) { // эта функция определяет, является ли строка «пустой» для логики флагов -b (нумерация только непустых) и -s (сжатие пустых строк). Важно: Строка считается пустой, даже если в ней есть символы перевода строки (\n) или возврата каретки (\r), но нет никакого видимого текста.
  bool empty = true; // По умолчанию считаем пустой
  for (size_t k = 0; line[k] != '\0'; k++) { // Бежим по всем символам
    if (line[k] != '\n' && line[k] != '\r') { // Если нашли что-то кроме переносов
      empty = false; // Значит, строка НЕ пустая
      break; // Можно прекращать проверку
    }
  }
  return empty;
}

static void cat_print_numbering(const CatFlags *flags, bool empty, // эта функция решает, печатать ли номер строки перед контентом, и какой именно номер использовать (общий или только для непустых).
                                int *line_num, int *non_empty_num) {
  if (flags->n || (flags->b && !empty)) { // условие: Печатать, если включен -n (всегда) ИЛИ (-b и строка не пустая)
    if (flags->b && !empty) (*non_empty_num)++; // eсли режим -b и строка не пустая -> увеличиваем счетчик непустых
    if (flags->n) (*line_num)++; // если режим -n -> увеличиваем общий счетчик
    int num = flags->n ? *line_num : *non_empty_num; // выбираем, какой номер показать: Если включен -n, берем общий счетчик. Иначе (значит включен -b) берем счетчик непустых.
    printf("%6d\t", num); // Печатаем: число шириной 6 знаков, затем таб
  }
}

static void cat_print_content(const char *line, const CatFlags *flags) { // зачем нужна: Посимвольный вывод содержимого строки с применением визуальных трансформаций (-e, -t, -v).
  for (size_t k = 0; line[k] != '\0'; k++) {
    unsigned char ch = line[k]; // Берем символ как беззнаковый
    if (ch == '\n') { // 1. Обработка конца строки
      if (flags->e || flags->E_flag)
        printf("$\n"); // Показать $ перед переносом
      else
        fputc('\n', stdout); // Просто перенос
    } else if (ch == '\t' && (flags->t || flags->T_flag)) { // 2. Обработка табуляции
      printf("^I"); // Показать ^I вместо таба
    } else if (flags->v && (ch < 32 || ch == 127 || ch > 127)) { // 3. Обработка остальных непечатных символов (только если есть -v)
      print_visual_char(ch, stdout); // Делегируем сложную логику
    } else { // 4. Обычные символы
      fputc(ch, stdout); // Печатаем как есть
    }
  }
}

static void cat_process_single_line(const char *line, const CatFlags *flags, // Зачем нужна: Это «диспетчер» для одной строки. Она решает главную дилемму флага -s (сжатие пустых строк): «Печатать эту строку или пропустить, потому что предыдущая тоже была пустой?».
                                    int *line_num, int *non_empty_num,
                                    bool *prev_empty) {
  bool empty = is_line_empty(line); // 1. Узнаем, пустая ли текущая строка
  bool should_skip = false;

  // 2. Логика флага -s
  if (flags->s && empty) { // Если флаг -s включен И строка пустая:
    if (*prev_empty) { // Если ПРЕДЫДУЩАЯ строка тоже была пустой -> ПРОПУСКАЕМ эту.
      should_skip = true;
    } else { // Если предыдущая была НЕ пустой -> это первая пустая строка в серии.
      *prev_empty = true; // Разрешаем её печать, но запоминаем состояние.
    }
  } else {
    *prev_empty = false;  // Если строка не пустая (или флаг -s выключен): Сбрасываем флаг "предыдущая была пустой". Следующая пустая станет "первой".
  }

  if (!should_skip) { // 3. Печать
    cat_print_numbering(flags, empty, line_num, non_empty_num);
    cat_print_content(line, flags);
  }
}

int execute_cat(CatFlags *flags, int file_start_idx, int argc, char *argv[]) { // Зачем нужна: Это главный организатор процесса. Она открывает файлы, читает их построчно и делегирует обработку каждой строки функции выше. Также она управляет памятью и кодами ошибок.
  int line_num = 0; // Счетчик всех строк (для -n)
  int non_empty_num = 0; // Счетчик непустых (для -b)
  bool prev_empty = false; // Флаг состояния для -s (важно: он сохраняется между строками!)
  int error_status = 0; // Код возврата (0 - успех, 1 - ошибка)

  if (file_start_idx >= argc) {
    error_status = 0; // Файлов нет — не ошибка, просто выходим
  } else {
    for (int i = file_start_idx; i < argc; i++) { // Цикл по всем файлам
      FILE *fp = fopen(argv[i], "r"); // Попытка открыть файл
      if (!fp) { // ОШИБКА: Файл не открылся
        fprintf(stderr, "s21_cat: %s: %s\n", argv[i], strerror(errno));
        error_status = 1; // Запоминаем, что была ошибка и НЕ прерываем цикл! Продолжаем читать следующие файлы (стандартное поведение cat)
      } else { // УСПЕХ: Файл открыт
        char *line = NULL;
        size_t len = 0;
        while (getline(&line, &len, fp) != -1) { // Читаем файл ПОСТРОЧНО до конца
          cat_process_single_line(line, flags, &line_num, &non_empty_num, // Обрабатываем одну строку, передавая ей ВСЕ счетчики и флаги по указателю
                                  &prev_empty);
        }
        free(line); // Освобождаем память, выделенную getline
        fclose(fp); // Закрываем файл
      }
    }
  }

  return error_status; // Возвращаем 0 или 1 в main
}

static bool grep_check_match(const GrepFlags *flags, const char *line) { // Зачем нужна: Это «детектор» совпадений для grep. Она проверяет, соответствует ли данная строка хотя бы одному из загруженных паттернов (логическое ИЛИ).
  bool match = false; // если хоть один паттерн будет найден, то поменяется на true
  for (int p = 0; p < flags->pattern_count; p++) { // Перебираем ВСЕ загруженные паттерны
    if (regexec(&flags->regexes[p], line, 0, NULL, 0) == 0) { // regexec запускает регулярное выражение. Аргументы: компилированный паттерн, строка для поиска, кол-во совпадений (0), массив результатов (NULL)
      match = true; // Если вернуло 0 — значит совпадение найдено!
      break; // Нашли хоть одно — дальше искать нет смысла, выходим
    }
  }
  return match;
}

static void grep_handle_flag_o(const GrepFlags *flags, const char *line,
                               int line_num, bool multiple_files,
                               const char *fname) {
  // Флаг: нашли ли мы уже подходящий паттерн из списка (чтобы не проверять остальные)
  bool found_pattern = false;

  // Цикл по всем загруженным паттернам (логическое ИЛИ: достаточно одного совпадения)
  // Останавливаемся, если паттерны кончились ИЛИ если уже нашли совпадение для этого флага
  for (int p = 0; p < flags->pattern_count && !found_pattern; p++) {
    
    // Массив структур для хранения координат найденных совпадений (начало и конец)
    // Выделяем место под 10 групп захвата (хотя для простого поиска нужна только 0-я группа)
    regmatch_t pmatch[10];

    // Пытаемся найти первое вхождение текущего паттерна (p) в строке (line)
    // Аргументы: компилированный regex, строка, кол-во групп (10), массив результатов, флаги (0)
    // Если regexec возвращает 0 — значит совпадение найдено
    if (regexec(&flags->regexes[p], line, 10, pmatch, 0) == 0) {
      
      // Смещение в строке, с которого начинаем поиск следующего вхождения
      // Изначально 0, так как ищем с начала строки
      int offset = 0;
      
      // Флаг: выводили ли мы уже хоть одно совпадение для этой строки (нужен для логики, хотя здесь дублируется)
      bool found_any = false;
      
      // Флаг остановки внутреннего цикла (защита от бесконечности и выход при конце строки)
      bool stop_loop = false;

      // ВНУТРЕННИЙ ЦИКЛ: Ищет ВСЕ вхождения одного и того же паттерна в одной строке
      // Условие: не стоп и regexec находит совпадение в остатке строки (line + offset)
      while (!stop_loop &&
             regexec(&flags->regexes[p], line + offset, 10, pmatch, 0) == 0) {
        
        // Блок вывода префикса (имя файла и номер строки)
        if (!found_any) {
          // Если это ПЕРВОЕ совпадение в строке:
          // Если файлов много и флаг -h (hide) выключен -> печатаем имя файла и двоеточие
          if (!flags->h && multiple_files) printf("%s:", fname);
          // Если включен флаг -n (number) -> печатаем номер строки и двоеточие
          if (flags->n) printf("%d:", line_num);
          // Помечаем, что начало вывода положено
          found_any = true;
        } else {
          // Если это ВТОРОЕ и последующие совпадения:
          // Стандарт grep -o требует выводить каждое совпадение с новой строки с полным заголовком
          // Поэтому снова печатаем имя файла (если нужно)
          if (!flags->h && multiple_files) printf("%s:", fname);
          // И снова печатаем номер строки
          if (flags->n) printf("%d:", line_num);
        }

        // Цикл печати самого текста совпадения посимвольно
        // j идет от начала совпадения (rm_so) до конца (rm_eo)
        for (int j = pmatch[0].rm_so; j < pmatch[0].rm_eo; j++) {
          // Печатаем символ из исходной строки
          // Индекс рассчитывается как: текущее смещение (offset) + индекс внутри найденного куска (j)
          putchar(line[offset + j]);
        }
        
        // После вывода текста совпадения обязательно делаем перенос строки
        // Так работает флаг -o: каждое совпадение с новой строки
        putchar('\n');

        // ОБНОВЛЕНИЕ СМЕЩЕНИЯ:
        // Сдвигаем offset на конец текущего найденного совпадения, чтобы искать следующее
        offset += pmatch[0].rm_eo;

        // ЗАЩИТА ОТ БЕСКОНЕЧНОГО ЦИКЛА:
        // Если длина совпадения 0 (start == end), то offset не изменится в следующем шаге логически,
        // и regexec будет находить то же самое место вечно.
        // Принудительно сдвигаем offset на 1 символ вперед.
        if (pmatch[0].rm_so == pmatch[0].rm_eo) offset++;

        // ПРОВЕРКА КОНЦА СТРОКИ:
        // Если смещение достигло или превысило длину всей строки -> останавливаем цикл
        if (offset >= (int)strlen(line)) stop_loop = true;
      }
      
      // Мы успешно обработали все вхождения текущего паттерна.
      // Ставим флаг, чтобы внешний цикл по паттернам (for p) прервался и не проверял остальные.
      found_pattern = true;
    }
  }
}

static int grep_process_file(const char *filename, const GrepFlags *flags,
                             bool multiple_files, bool *any_match) {
  // Переменная для хранения статуса ошибки этого файла (0 - ок, 2 - ошибка открытия)
  int file_error = 0;

  // Пытаемся открыть файл только для чтения ("r")
  FILE *fp = fopen(filename, "r");

  // Если fopen вернул NULL, значит файл не открыт (не существует или нет прав)
  if (!fp) {
    // Если флаг -s (silent) НЕ включен, выводим сообщение об ошибке в stderr
    if (!flags->s) {
      // Выводим: имя программы: имя_файла: описание_ошибки
      fprintf(stderr, "s21_grep: %s: %s\n", filename, strerror(errno));
    }
    // Устанавливаем код ошибки 2 (стандартный код для ошибок доступа в grep)
    file_error = 2;
  } else {
    // ЕСЛИ ФАЙЛ ОТКРЫТ УСПЕШНО:

    // Объявляем указатель на буфер строки (изначально NULL, память выделит getline)
    char *line = NULL;
    // Переменная для хранения размера выделенного буфера
    size_t len = 0;
    // Счетчик номеров строк внутри этого файла
    int line_num = 0;
    // Счетчик найденных совпадений в этом файле (нужен для флага -c)
    int file_matches = 0;
    // Флаг досрочного завершения чтения (нужен для флага -l)
    bool finished = false;

    // Цикл чтения файла построчно:
    // Выполняется, пока не установлен флаг finished И getline успешно читает строку (!= -1)
    while (!finished && getline(&line, &len, fp) != -1) {
      // Увеличиваем номер текущей строки
      line_num++;
      
      // Получаем длину прочитанной строки
      size_t ln = strlen(line);
      // Удаляем символ переноса строки '\n' в конце, если он есть (getline его сохраняет)
      // Это нужно для корректной работы регулярных выражений и вывода
      if (ln > 0 && line[ln - 1] == '\n') line[ln - 1] = '\0';

      // Проверяем, соответствует ли строка хоть одному паттерну
      bool match = grep_check_match(flags, line);
      
      // Если включен флаг -v (invert), инвертируем результат совпадения
      if (flags->v) match = !match;

      // Если итоговый результат — совпадение (с учетом инверсии):
      if (match) {
        // Глобальный флаг: хоть одно совпадение в проекте найдено
        *any_match = true;
        // Локальный счетчик совпадений в этом файле
        file_matches++;

        // ЛОГИКА ВЫВОДА В ЗАВИСИМОСТИ ОТ ФЛАГОВ:

        // Если флаг -l (только имена файлов):
        if (flags->l) {
          // Печатаем имя файла один раз
          printf("%s\n", filename);
          // Больше этот файл читать не нужно, ставим флаг завершения
          finished = true;
        
        // Если НЕ флаг -c (count) и НЕ флаг -o (only-matching) -> обычный вывод строк
        } else if (!flags->c && !flags->o) {
          // Если файлов много и не стоит -h, печатаем имя файла перед строкой
          if (!flags->h && multiple_files) printf("%s:", filename);
          // Если стоит -n, печатаем номер строки
          if (flags->n) printf("%d:", line_num);
          // Печатаем саму строку и перенос
          printf("%s\n", line);
        
        // Если флаг -o (только совпадения) и НЕ инверсия (-v с -o не дружит в нашей логике)
        } else if (flags->o && !flags->v) {
          // Вызываем специальную функцию для вывода только совпадающих частей
          grep_handle_flag_o(flags, line, line_num, multiple_files, filename);
        }
      }
    }

    // ПОСЛЕ ОКОНЧАНИЯ ЧТЕНИЯ ФАЙЛА:

    // Если включен флаг -c (count):
    if (flags->c) {
      // Если файлов много и не стоит -h, печатаем имя файла
      if (!flags->h && multiple_files) printf("%s:", filename);
      // Печатаем общее количество совпадений в этом файле
      printf("%d\n", file_matches);
    }

    // Освобождаем память, выделенную функцией getline для буфера строки
    free(line);
    // Закрываем файл
    fclose(fp);
  }

  // Возвращаем статус ошибки (0 или 2)
  return file_error;
}

int execute_grep(GrepFlags *flags, int file_start_idx, int argc, char *argv[]) {
  // Итоговый статус возврата программы (0 - успех, 1 - нет совпадений, 2 - ошибка)
  int final_status = 0;
  
  // Глобальный флаг: было ли найдено хоть одно совпадение во всех файлах
  bool any_match = false;
  
  // Флаг ошибки компиляции регулярных выражений
  int compile_error = 0;

  // Проверка: если паттерны не заданы вообще
  if (!flags->e || flags->pattern_count == 0) {
    // Код ошибки 2 (ошибка использования)
    final_status = 2;
  } else {
    // Настройка флагов для компиляции regex:
    // REG_EXTENDED включает расширенный синтаксис (как в egrep)
    int cflags = REG_EXTENDED;
    
    // Если включен флаг -i (ignore-case), добавляем флаг игнорирования регистра
    if (flags->i) cflags |= REG_ICASE;

    // ЦИКЛ КОМПИЛЯЦИИ ПАТТЕРНОВ:
    // Проходим по всем заданным паттернам
    for (int p = 0; p < flags->pattern_count; p++) {
      // Пытаемся скомпилировать строку-паттерн во внутренний формат regex
      if (regcomp(&flags->regexes[p], flags->patterns[p], cflags) != 0) {
        // Если компиляция не удалась (возврат != 0):
        fprintf(stderr, "s21_grep: Invalid regex pattern\n");
        compile_error = 1; // Ставим флаг ошибки
        break; // Прерываем цикл, дальше компилировать нет смысла
      }
    }

    // Если ошибок компиляции НЕ было:
    if (!compile_error) {
      // Определяем, работаем ли мы с несколькими файлами (для вывода имен файлов)
      bool multiple_files = (argc - file_start_idx > 1);
      
      // ЦИКЛ ОБРАБОТКИ ФАЙЛОВ:
      // Проходим по всем именам файлов, начиная с file_start_idx
      for (int i = file_start_idx; i < argc; i++) {
        // Вызываем функцию обработки одного файла
        // Передаем адрес глобального флага any_match, чтобы функция могла его изменить
        int file_status =
            grep_process_file(argv[i], flags, multiple_files, &any_match);
        
        // Если функция вернула ошибку (код 2), запоминаем это в итоговом статусе
        if (file_status == 2) {
          final_status = 2;
        }
      }

      // ФИНАЛЬНАЯ ЛОГИКА СТАТУСА:
      // Если ошибок файлов не было (status 0), но совпадений не найдено вообще (any_match false)
      if (final_status == 0 && !any_match) {
        // Возвращаем 1 (стандартный код "совпадений не найдено")
        final_status = 1;
      }
    } else {
      // Если была ошибка компиляции, итоговый статус — 2
      final_status = 2;
    }

    // ОЧИСТКА ПАМЯТИ РЕГУЛЯРНЫХ ВЫРАЖЕНИЙ:
    // Проходим по всем скомпилированным паттернам
    for (int p = 0; p < flags->pattern_count; p++) {
      // Освобождаем внутреннюю структуру regex (обязательно после regcomp)
      regfree(&flags->regexes[p]);
    }
    // Примечание: освобождение памяти самих строк паттернов (если они из -f) 
    // вынесено в main, так как там известна их природа (strdup).
  }

  // Возвращаем итоговый код состояния программе
  return final_status;
}