#define _POSIX_C_SOURCE 200809L
// Снова подключаем стандарт POSIX для корректной работы системных функций

#include <stdbool.h> // Для bool
#include <stdio.h>   // Для printf, fprintf
#include <stdlib.h>  // Для free
#include <string.h>  // Для memset (хотя здесь он в utils, но на всякий случай)

// Подключаем наш общий модуль, где описаны структуры и функции
#include "../common/s21_utils.h"

int main(int argc, char *argv[]) {
  // Переменная для кода возврата программы (0 - успех, 2 - ошибка использования/доступа)
  int exit_code = 0;

  // Проверка: если аргументов меньше 2 (только имя программы), значит нет ни флага, ни паттерна
  if (argc < 2) {
    // Выводим инструкцию по использованию в поток ошибок (stderr)
    fprintf(stderr, "Usage: s21_grep [options] PATTERN [FILE...]\n");
    // Устанавливаем код ошибки 2 (стандарт для неправильного использования CLI)
    exit_code = 2;
  } else {
    // ЕСЛИ АРГУМЕНТЫ ЕСТЬ:

    // Объявляем структуру для хранения всех флагов и данных grep
    GrepFlags flags;
    
    // Локальная переменная для подсчета количества паттернов во время парсинга
    int pattern_count = 0;
    
    // Индекс первого файла в массиве argv. Изначально ставим за границу (argc),
    // что означает "файлы не найдены".
    int file_start_idx = argc;

    // Вызываем функцию парсинга аргументов.
    // Передаем адреса переменных (&flags, &pattern_count, &file_start_idx),
    // чтобы функция могла изменить их значения.
    parse_grep_flags_main(argc, argv, &flags, &pattern_count, &file_start_idx);

    // Сохраняем посчитанное количество паттернов в структуру для дальнейшего использования
    flags.pattern_count = pattern_count;

    // Проверка: если флаг -e не установлен ИЛИ количество паттернов равно 0
    if (!flags.e || flags.pattern_count == 0) {
      // Значит паттерн не был задан вообще
      fprintf(stderr, "s21_grep: No pattern provided\n");
      exit_code = 2; // Код ошибки: отсутствие обязательного операнда
    } else {
      // ЕСЛИ ПАТТЕРН ЗАДАН:

      // Если индекс первого файла не равен argc, значит файлы были найдены
      if (file_start_idx != argc) {
        // Запускаем основную функцию выполнения grep.
        // Возвращаемое значение (статус ошибки) записываем в exit_code.
        exit_code = execute_grep(&flags, file_start_idx, argc, argv);
      }
      
      // ОЧИСТКА ПАМЯТИ (CRITICAL STEP):
      // Проходим по всем сохраненным паттернам
      for (int k = 0; k < flags.pattern_count; k++) {
        // Проверяем флаг: нужно ли освобождать этот конкретный паттерн?
        // (true только для тех, что были загружены через -f и созданы через strdup)
        if (flags.pattern_needs_free[k]) {
          // Освобождаем память, выделенную в куче
          free(flags.patterns[k]);
        }
        // Паттерны из argv (флаг -e или позиционные) имеют false, поэтому free не вызывается.
        // Это предотвращает краш программы (double free).
      }
    }
  }
  
  // Возвращаем итоговый код состояния операционной системе
  return exit_code;
}