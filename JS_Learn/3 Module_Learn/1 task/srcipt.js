let dataСleaning = function (data) { // функция, которая принимает массив сырых транзакций и преобразует его в массив чистых чисел
    let cleatArray = [] // массив чистых чисел
    for (let i of data) { // запускаю цикл, который проходится по каждому элементу массива сырых транзакций
        if (!Number.isNaN(i)) { // проверяю чтобы значение было не равно NaN
            i = String(i) // преобразовываю каждый элемент массива в строку
            let currencyCleaning = i.indexOf("$") // переменная которая хранит позицию "$" для того чтобы в будущем её можно было использовать как начальную позицию для slice, для того чтобы вывести транзакцию, где в начале есть "$"
            if (~currencyCleaning && i !== "Infinity") { // использую трюк с побитовым НЕ чтобы проверить есть ли у значения "$", а также ещё проверяю, чтобы бы значение было не Infinity
                cleatArray.push(parseFloat(i.slice(currencyCleaning + 1).trim())) // вывожу только то, что идёт после "$", а также использую trim, если вдруг у нас есть лишний пробел, например после "$"(как в последней транзакции) и в конце оборачиваю всё в parseFloat для извлечения только числа из строки, если например у нас после числа что-то ещё идёт(например, наименование валюты(USD, RUB и т. д.))
            } else if (!isNaN(parseFloat(i)) && i !== "Infinity") { // если у значения нет "$", то мы заходим в эту часть условной конструкции и просто извлекаем число из строки с помощью parseFloat и проверяем не вернули ли parseFloat NaN с помошью isNaN и если NaN = true, то меняем его на false с помошью ! и так же проверяем, чтобы бы значение было не Infinity
                cleatArray.push(parseFloat(i))
            } else { // иначе если мы ни, где не попали под условие, то значит значение не удалось преобразовать в конечное число, поэтому заменяем его на 0
                i = 0
                cleatArray.push(i)
            }
        } else { // иначе если значение равно NaN, то мы заменяем его на ноль
            i = 0
            cleatArray.push(i)
        }
    }
    return cleatArray
}

function transactionAmount(dataArray) { // функция суммирования всех транзакций
    return dataArray.reduce((acc, curr) => acc + curr, 0) // суммирую все транзакции с помощью reduce
}

