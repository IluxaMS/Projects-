function specificWords(arrayData) {
    let arrayMatchingDescriptions = [] // массив для подходящих описаний
    for (let i of arrayData) {
        if (i.slice(0, 7) === "Покупка" || i.slice(-3) === "USD") { // нахожу все описания которые начинаются с слова "Покупка" или заканчиваются на "USD" - для этого использую slice
            if (i.includes("VIP")) { // проверяю содержится ли в каком-либо описании(из тех, что подходят под условие выше) подстрока "VIP" - для этого использую includes, так как он в отличие от indexOf, возвращает true либо false, а это то, что нам как раз нужно для if
                console.log(`Позиция первого вхождения слова "VIP" у описания "${i}": ${i.indexOf("VIP")}`) // для вывода позиции первого вхождения подстроки "VIP", в том или ином описании, я использую indexOf, так как он, в отличие от includes, возвращает позицию первого вхождения подстроки
            } else { // иначе просто вывожу логический вывод 
                console.log(`У транзакции "${i}" в описание нет слова VIP`)
            }
            arrayMatchingDescriptions.push(i.toUpperCase()) // добавляю каждое подходящее описание в отдельный массив, при этом делаю каждое описание в верхнем регистре, тем самым учитываю разные регистры при будущем языковом сравнение
        }
    }

    arrayMatchingDescriptions.sort((a, b) => a.localeCompare(b)) // соритирую описания в алфавитном порядке - для этого использую localeCompare

    return arrayMatchingDescriptions
}

