#define _GNU_SOURCE           // Включаем расширения GNU (нужно для функции getline)
#define _POSIX_C_SOURCE 200809L // Включаем стандарт POSIX.1-2008 (для переносимости и некоторых функций)

#include <errno.h>            // Подключаем обработку ошибок системы (переменная errno)
#include <getopt.h>           // Подключаем библиотеку для разбора аргументов командной строки (getopt)
#include <stdbool.h>          // Подключаем тип bool (true/false)
#include <stdio.h>            // Подключаем стандартный ввод/вывод (printf, fopen, stdin)
#include <stdlib.h>           // Подключаем общие функции (malloc, free, exit)
#include <string.h>           // Подключаем функции для работы со строками (strerror, strlen)

// Объявление структуры для хранения состояния всех флагов утилиты cat
typedef struct {
  int b; // Флаг -b: нумерует только непустые строки (имеет приоритет над -n)
  int e; // Флаг -e: комбо-флаг. 1. Показывает $ в конце строки (как -E). 2. Визуализирует управляющие символы (как -v)
  int E; // Флаг -E (GNU): показывает только $ в конце строки, НЕ включает визуализацию других символов
  int n; // Флаг -n: нумерует ВСЕ строки подряд, включая пустые
  int s; // Флаг -s: сжимает серии из нескольких пустых строк в одну пустую строку
  int t; // Флаг -t: комбо-флаг. 1. Показывает табы как ^I (как -T). 2. Визуализирует управляющие символы (как -v)
  int T; // Флаг -T (GNU): показывает только табы как ^I, НЕ включает визуализацию других символов
  int v; // Флаг -v: делает видимыми все непечатные символы ASCII (кроме таба и конца строки)
} CatFlags;

// Функция обработки одного специального символа. Возвращает true, если символ был обработан (заменен)
static bool handle_special_char(unsigned char c, const CatFlags *flags) {
  bool handled = false; // Флаг: был ли символ заменен на визуальное представление
  
  // Если символ — таб и включен флаг -t или -T
  if (c == '\t' && (flags->t || flags->T)) { 
    printf("^I");       // Печатаем визуальное обозначение таба (^I)
    handled = true;     // Помечаем, что символ обработан
  } 
  // Если включен режим визуализации (-v) или режим конца строк+символов (-e)
  else if (flags->v || flags->e) { 
    // Если это управляющий символ ASCII (код < 32), но не перевод строки и не таб
    if (c < 32 && c != '\n' && c != '\t') {
      printf("^%c", c + 64); // Печатаем как ^X (сдвигаем код на 64, чтобы получить букву)
      handled = true;        // Помечаем как обработанный
    } 
    // Если это расширенный символ (код 127-159), часто встречающийся в старых кодировках
    else if (c > 126 && c < 160) { 
      printf("M-^%c", c - 128 + 64); // Печатаем как M-^X (сбрасываем старший бит и сдвигаем)
      handled = true;                // Помечаем как обработанный
    } 
    // Если это символ DEL (код 127)
    else if (c == 127) { 
      printf("^?");       // Печатаем визуальное обозначение удаления (^?)
      handled = true;     // Помечаем как обработанный
    }
  }

  return handled; // Возвращаем результат: true если заменили, false если печатать как есть
}

// Функция печати одной строки с учетом всех флагов и счетчиков
static void print_line(const char *line, size_t len, const CatFlags *flags,
                       int *line_num, int *non_blank_num, bool *prev_blank) {
  // Определяем, является ли строка пустой (длина 0 или только один символ \n)
  bool is_blank = (len == 0 || (len == 1 && line[0] == '\n'));
  bool should_process = true; // Флаг: нужно ли вообще выводить эту строку

  // Логика флага -s (сжатие пустых строк)
  if (flags->s && is_blank && *prev_blank) {
    should_process = false; // Если текущая пустая И предыдущая была пустая -> пропускаем вывод
  } else {
    *prev_blank = is_blank; // Запоминаем состояние текущей строки для следующей итерации
  }

  // Если строку нужно выводить (не попала под сжатие -s)
  if (should_process) {
    int current_num = 0; // Переменная для текущего номера строки
    
    // Логика нумерации
    if (flags->b) { // Если флаг -b (нумерация непустых)
      if (!is_blank) { // И если строка не пустая
        (*non_blank_num)++; // Увеличиваем счетчик непустых
        current_num = *non_blank_num; // Берем этот номер
      }
    } else if (flags->n) { // Иначе, если флаг -n (нумерация всех)
      (*line_num)++;       // Увеличиваем общий счетчик
      current_num = *line_num; // Берем этот номер
    }

    // Если номер был присвоен (значит флаг нумерации активен и условия выполнены)
    if (current_num > 0) {
      printf("%6d\t", current_num); // Печатаем номер шириной 6 знаков и таб
    }

    // Посимвольный проход по строке для вывода содержимого
    for (size_t i = 0; i < len; i++) {
      unsigned char c = line[i]; // Берем текущий символ
      // Пытаемся обработать его как специальный (таб, управляющий символ)
      bool char_handled = handle_special_char(c, flags);

      // Если символ НЕ был обработан функцией выше (значит это обычный текст или \n)
      if (!char_handled) {
        // Если это конец строки И включен флаг показа концов (-e или -E)
        if (c == '\n' && (flags->e || flags->E)) {
          printf("$\n"); // Печатаем доллар и реальный перенос строки
        } else {
          putchar(c);    // Иначе печатаем символ как есть
        }
      }
    }
  }
}

// Функция обработки всего файла: чтение построчно и передача каждой строки в print_line
static void process_file(FILE *fp, const CatFlags *flags, int *line_num,
                         int *non_blank_num, bool *prev_blank) {
  char *line = NULL; // Указатель на буфер строки (getline выделит память сам)
  size_t len = 0;    // Размер выделенного буфера
  ssize_t read;      // Количество прочитанных байт

  // Читаем файл построчно, пока getline возвращает не -1 (конец файла или ошибка)
  while ((read = getline(&line, &len, fp)) != -1) {
    // Передаем строку на печать, приводя длину к типу size_t
    print_line(line, (size_t)read, flags, line_num, non_blank_num, prev_blank);
  }
  free(line); // Освобождаем память, выделенную функцией getline (важно для отсутствия утечек!)
}

int main(int argc, char *argv[]) {
  CatFlags flags = {0}; // Создаем структуру флагов и обнуляем все поля (безопасная инициализация)
  int opt;              // Переменная для хранения текущего распознанного флага
  int exit_code = 0;    // Код возврата программы (0 = успех)

  // Цикл разбора аргументов командной строки с помощью getopt
  // "beEnstTv" — строка допустимых опций
  while ((opt = getopt(argc, argv, "beEnstTv")) != -1) {
    switch (opt) { // Выбираем действие в зависимости от найденного флага
    case 'b':
      flags.b = 1; // Включаем флаг нумерации непустых
      break;
    case 'e':
      flags.e = 1; // Включаем флаг показа концов и спецсимволов
      flags.v = 1; // -e автоматически включает режим визуализации (-v)
      break;
    case 'E':
      flags.E = 1; // Включаем только показ концов строк (GNU стиль)
      break;
    case 'n':
      flags.n = 1; // Включаем полную нумерацию
      break;
    case 's':
      flags.s = 1; // Включаем сжатие пустых строк
      break;
    case 't':
      flags.t = 1; // Включаем показ табов
      flags.v = 1; // -t автоматически включает режим визуализации (-v)
      break;
    case 'T':
      flags.T = 1; // Включаем только показ табов (GNU стиль)
      break;
    case 'v':
      flags.v = 1; // Включаем режим визуализации
      break;
    default: // Если введен неизвестный флаг
      fprintf(stderr, "Usage: %s [-beEnstTv] [file ...]\n", argv[0]); // Печатаем инструкцию в stderr
      exit_code = 1; // Устанавливаем код ошибки
      optind = argc; // Принудительно завершаем цикл разбора аргументов
      break;
    }
  }

  // Если при разборе аргументов не было ошибок (exit_code == 0)
  if (exit_code == 0) {
    int line_num = 0;       // Счетчик всех строк (для -n)
    int non_blank_num = 0;  // Счетчик непустых строк (для -b)
    bool prev_blank = false; // Флаг состояния: была ли предыдущая строка пустой (для -s)

    // Если файлы не были указаны в аргументах (optind >= argc)
    if (optind >= argc) {
      // Читаем из стандартного ввода (stdin)
      process_file(stdin, &flags, &line_num, &non_blank_num, &prev_blank);
    } else {
      // Если файлы указаны, проходим циклом по всем именам файлов
      for (int i = optind; i < argc; i++) {
        FILE *fp = fopen(argv[i], "r"); // Пытаемся открыть файл для чтения
        if (fp == NULL) { // Если файл не открылся (ошибка)
          if (!flags.s) { // Если НЕ включен режим тишины (-s)
            // Печатаем ошибку: имя файла и описание ошибки из системы
            fprintf(stderr, "cat: %s: %s\n", argv[i], strerror(errno));
          }
          exit_code = 1; // Устанавливаем код ошибки выполнения
          continue;      // Переходим к следующему файлу, не прерывая программу
        }
        // Обрабатываем открытый файл
        process_file(fp, &flags, &line_num, &non_blank_num, &prev_blank);
        fclose(fp); // Закрываем файл после обработки
      }
    }
  }

  return exit_code; // Возвращаем код статуса операционной системе (0 успех, 1 ошибка)
}