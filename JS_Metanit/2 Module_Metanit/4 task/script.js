/* Задание 4: «Система Логирования с Переопределением»
Создайте модуль логирования, который позволяет гибко менять способ вывода сообщений (в консоль, в alert, или в скрытый массив) без изменения основного кода программы.
Требования к логике:
Объявите функцию logger(message), которая по умолчанию просто выводит сообщение в консоль с префиксом [INFO].
Реализуйте функцию setoggerMode(mode), которая принимает строку ('console', 'alert', 'storage').
В зависимости от режима, функция setLoggerMode должна переопределять поведение функции logger:
'console': стандартный console.log.
'alert': вывод через alert (или эмуляцию через подтверждение).
'storage': сохранение всех сообщений в специальный массив (историю), доступный для чтения, но не выводимый сразу.
Протестируйте систему: отправьте несколько логов в режиме консоли, затем переключите режим на 'storage', отправьте еще несколько, и затем выведите накопленную историю.
Используйте стрелочную функцию для быстрой проверки сообщения перед логированием (например, если сообщение пустое — не логировать).
Обратите внимание на область видимости массива истории: он должен быть доступен функции logger после переопределения, но не быть глобальной переменной (используйте замыкание или блочную область).
Цель: переопределение функций, динамическое изменение поведения, замыкания для хранения состояния, стрелочные функции. */

function loggingSystemWithOverride() {
    let history = [] // доступен функции logger после переопределения, но не глобальный(вне loggingSystemWithOverride не доступна)
    let currentLogStrategy = (message) => {
        console.log('[INFO]', message); // по умолчанию просто выводит сообщение в консоль с префиксом [INFO]
    };

    function logger(message) { // объявление функцию logger(message)
        const isValid = msg => msg !== "" && msg !== null; // стрелочная функция для быстрой проверки сообщения перед логированием
        if (!isValid(message)) return;
        currentLogStrategy(message);
    }

    function setLoggerMode(mode) { // функция setoggerMode(mode), которая принимает строки ('console', 'alert', 'storage')
        switch (mode) { // в зависимости от режима, функция setLoggerMode переопределяет поведение функции logger
            case "console":
                currentLogStrategy = (message) => {
                    console.log('[INFO] log', message); // стандартный console.log
                };
                break
            case "alert":
                currentLogStrategy = (message) => {
                    // alert('[INFO] ' + message) // вывод через alert (или эмуляцию через подтверждение)
                    console.log('[INFO] alert', message)  // эмуляция alert
                };
                break
            case "storage":
                currentLogStrategy = (message) => {
                    console.log("push")
                    history.push(message) // сохранение всех сообщений в специальный массив
                };
                break
            default:
                console.log("Были введены не корректные данные")
        }
    }

    // тестирование системы
    logger("Сообщение 1"); // работает консоль (по умолчанию)
    logger("Сообщение 2");
    setLoggerMode("storage"); // переключаем режим
    logger("Сообщение 3"); // автоматически уходит в хранилище
    logger("Сообщение 4");
    console.log(history) // вывод накопленной истории
}

