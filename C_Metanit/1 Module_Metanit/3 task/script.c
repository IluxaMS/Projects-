/* Задание 3: «Статистика оценок»
Уровень: Средний
Основные темы: Массивы, циклы, константы, арифметика, ввод строк/чисел.
Напишите программу для обработки оценок студента.
Задайте константу, определяющую максимальное количество оценок (например, 5).
Запросите у пользователя имя студента (строка) и затем последовательно введите оценки (целые числа от 2 до 5) в массив.
Используя цикл, найдите среднюю оценку, минимальную и максимальную оценку в массиве.
Выведите имя студента и рассчитанную статистику.
Подсказка: Для ввода имени используйте подходящий метод, учитывающий, что имя может состоять из одного слова без пробелов, либо используйте технику очистки буфера ввода, если решите использовать посимвольный ввод. */
#include <stdio.h>
#include <string.h>

int main() {
    const double maximum_number_ratings = 10; // константа, определяющая максимальное количество оценок
    int array[10] = {};
    char name_student[50];
    double sum_grade = 0;
    int min_grade = 5;
    int max_grade;
    int rtrn = 0; // переменная для того, чтобы соответствовать одному из принципов структурного программирования - не более одного выхода из функции

    printf("Введите имя студента: ");
    fgets(name_student, sizeof(name_student), stdin); // запрашиваю у пользователя имя студента (строка) с помощью fgets, для ввода строки с пробелами

    size_t len = strlen(name_student);
    if (len > 0 && name_student[len - 1] == '\n') { // убираю перевод строки после fgets
        name_student[len - 1] = '\0';
    }

    printf("Введите оценки студента(не больше 10): ");
    scanf("%d %d %d %d %d %d %d %d %d %d", &array[0], &array[1], &array[2], &array[3], &array[4], &array[5], &array[6], &array[7], &array[8], &array[9]); // запрашиваю у пользователя последовательно оценки (целые числа от 2 до 5) в массив

    for (int i = 0; i < 10; i++) { // перебираем массив для того, чтобы найти среднюю, максимальную и минимальную оценки, а также убедится, что пользователь не ввёл оценки либо меньше 2, либо больше 5
        if (array[i] < 2 || array[i] > 6) {
            printf("Ошибка! - Вы ввели число, которое либо меньше 2, либо больше 5\n");
            rtrn = 1;
        } else if (rtrn != 1) {
            sum_grade += array[i]; // поиск средней оценки
            if (array[i] < min_grade) { // поиск минимальной оценки
                min_grade = array[i];
            } else {
                continue;
            }
        } else {
            break;
        }
    }

    max_grade = min_grade;

    for (int i = 0; i < 10; i++) {
        if (array[i] > max_grade && rtrn != 1) { // поиск максимальной оценки
            max_grade = array[i];
        } else if (rtrn == 1) {
            break;
        } else {
            continue;
        }
    }

    if (rtrn != 1) {
        // вывожу имя студента и рассчитанную статистику
        printf("У %s средняя оценка: %.2lf\n", name_student, sum_grade / 10);
        printf("У %s минимальная оценка: %d\n", name_student, min_grade);
        printf("У %s максимальная оценка: %d\n", name_student, max_grade);
    }

    return rtrn;
}