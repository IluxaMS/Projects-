/* Задание 4: «Шифратор сообщений»
Уровень: Выше среднего
Основные темы: Строки (массивы символов), циклы, поразрядные операции, работа с символами, условные операции внутри цикла.
Напишите программу, которая реализует простой шифр Цезаря с использованием поразрядной операции XOR.
Запросите у пользователя короткое сообщение (строку) и ключ шифрования (целое число, например, от 1 до 9).
Пройдитесь по каждому символу строки в цикле.
Примените к коду каждого символа операцию побитового исключающего ИЛИ (^) с ключом.
Соберите зашифрованную строку в новый массив символов. Не забудьте про завершающий нулевой символ \0.
Выведите исходное сообщение и зашифрованное.
Дополнительно: Напишите вторую часть программы (или тот же цикл повторно), которая расшифровывает сообщение обратно, применяя ту же операцию XOR с тем же ключом, и выводит восстановленный текст для проверки корректности. */
#include <stdio.h>
#include <string.h>

int main() {
    char short_message[20];
    int encryption_key;
    char array[20] = {};
    int rtrn = 0; // переменная для того, чтобы соответствовать одному из принципов структурного программирования - не более одного выхода из функции

    printf("Введите короткое сообщение: ");
    fgets(short_message, sizeof(short_message), stdin); // запрашиваю у пользователя короткое сообщение (строку) с помощью fgets, для ввода строки с пробелами

    size_t len = strlen(short_message);
    if (len > 0 && short_message[len - 1] == '\n') { // убираю перевод строки после fgets
        short_message[len - 1] = '\0';
        len--;
    }

    printf("Введите ключ шифрования: ");
    scanf("%d", &encryption_key); // запрашиваю у пользователя ключ шифрования

    if (encryption_key < 1 || encryption_key > 9) { // если ключ шифрования меньше 1, либо больше 9, то выдаю ошибку
        printf("Ошибка! - Вы ввели число, которое либо меньше 1, либо больше 9\n");
        rtrn = 1;
    } else if (rtrn != 1) {
        for (int i = 0; i < len; i++) { // прохожусь по каждому символу строки в цикле
            array[i] = short_message[i] ^ encryption_key; // применяю к коду каждого символа операцию побитового исключающего ИЛИ (^) с ключом + сразу же собираю зашифрованную строку в новый массив символов
        }

        printf("Исходное сообщение: %s\n", short_message);
        printf("Зашифрованное сообщение (символы): %s\n", array); // зашифрованное сообщение символами

        printf("Зашифрованное сообщение (коды): ");
        for (int i = 0; i < len; i++) {
            printf("%d ", array[i]); // зашифрованное сообщение кодами
        }
        printf("\n");
    }

    return rtrn;
}