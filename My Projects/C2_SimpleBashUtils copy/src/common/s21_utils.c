void print_visual_char(unsigned char c, FILE *out) { // эта функция реализует флаги -v -e -t . Если использовать просто char (который может быть знаковым), символы с кодом >127 станут 
  // отрицательными числами, и логика сравнения сломается. unsigned гарантирует диапазон 0–255
  if (c == '\t') { // "/t" - символ табуляции. Если это Таб -> покажи ^I.
    fprintf(out, "^I"); // fprintf работает аналогично printf, но первым аргументом принимает указатель на файл (FILE*), открытый для записи, позволяя сохранять структурированный текст, числа и переменные
  } else if (c == '\n') {
    fputc('\n', out); // fputc это стандартная функция библиотеки stdio.h, которая записывает один символ (char) в указанный поток вывода (файл или stdout). 
    // Если это Конец строки -> покажи перенос (а флаг -e добавит $ перед этим в другом месте кода).
  } else if (c < 32 || c == 127) { // Код BEL = 7. 7 + 64 = 71 ('G'). Вывод: ^G. Если это Невидимый управляющий символ (код < 32) -> покажи как ^Буква.
    fprintf(out, "^%c", c + 64);
  } else if (c > 127) {
    fprintf(out, "M-%c", c - 128 + 64); // Если это 8-битный символ (код > 127) -> покажи как M-Буква.
  } else {
    fputc(c, out); // Если это Обычный текст -> покажи как есть.
  }
}

// В утилите cat эти флаги используются для визуализации невидимых символов в тексте, таких как знаки табуляции или концы строк.
// -v (--show-nonprinting): Отображает непечатаемые символы (кроме табуляции и символов конца строки), используя нотацию ^ и M-. Например, Ctrl+G будет показано как ^G.
// -t: Эквивалентен комбинации -vT. Он отображает непечатаемые символы и дополнительно заменяет символы табуляции на ^I.
// -e: Эквивалентен комбинации -vE. Он отображает непечатаемые символы и ставит символ $ в конце каждой строки, чтобы было видно, где именно она заканчивается. 

// Краткая сводка:
// -v	Показывает управляющие символы (кроме Tab и конца строки).
// -T	Показывает только символы табуляции как ^I.
// -t	Показывает всё, что и -v, плюс табуляцию (-vT).
// -E	Показывает только концы строк как $.
// -e	Показывает всё, что и -v, плюс концы строк (-vE).
// -A	Показывает вообще всё (эквивалент -vET).


// Проблема: В обычном текстовом редакторе сложно понять, почему текст 
// «поехал» — из-за лишних пробелов или из-за скрытых знаков табуляции. Именно для этого в cat нужны флаги -v, -t и -e, 
// которые превращают невидимое в видимое (например, превращают TAB в ^I).

void parse_cat_flags(int argc, char *argv[], CatFlags *flags) { // задача функции - изменить состояние структуры flags, переданной по указателю. Результат виден сразу в вызывающей функции (main).
  // CatFlags *flags: Указатель на структуру, где мы будем хранить настройки
  memset(flags, 0, sizeof(CatFlags)); // memset гарантирует, что все флаги (b, e, n, s, t, v и т.д.) изначально равны false (0). Это фундамент безопасности.
  for (int i = 1; i < argc; i++) { // проверяем каждое слово, которое пользователь ввел в командной строке(кроме первого, так как это название имя программы(./s21_cat например), для этого мы прописываем, что i = 1 изначально, тем самым пропуская имя программы)
    if (argv[i][0] == '-') { // argv[i][0]: Берем первый символ текущего аргумента. Если аргумент начинается не с - (например, test.txt), значит, это имя файла. 
      // Мы его пропускаем в этой функции, так как parse_cat_flags занимается только настройками, а не файлами.
      for (int j = 1; argv[i][j] != '\0'; j++) {
        switch (argv[i][j]) {
          case 'b': // нумерация не пустых строк
            flags->b = true;
            break;
          case 'e': // отображение конца строки
            flags->e = true;
            flags->v = true;
            break;
          case 'E': // Зачем нужен flags->E_flag? Чтобы в функции вывода мы могли отличить: пользователь хотел просто показать $ (флаг -E) или показать $ и все остальные непечатные символы (флаг -e)?
            flags->E_flag = true;
            flags->e = true;
            break;
          case 'n': // Если пользователь введет -n -b, оба флага будут true. В функции execute_cat мы должны будем решить, какой приоритетнее. 
          // Обычно -n перекрывает -b (нумеруются все), либо они работают в сумме. Стандарт cat обычно трактует это так: если есть -n, он действует для всех строк. Наша реализация в execute_cat учтет это.
            flags->n = true;
            break;
          case 's': // нумерация всех строк
            flags->s = true;
            break;
          case 't': // Показывать табы как ^I.
            flags->t = true;
            flags->v = true;
            break;
          case 'T': // Показывать табы как ^I. Включаем обработку табов (t = true), но не включаем автоматом общую визуализацию (v). Это позволяет показать только табы, оставив другие спецсимволы скрытыми.
            flags->T_flag = true;
            flags->t = true;
            break;
          case 'v': // Включает режим отображения всех непечатных символов в читаемом виде (^X, M-X).
            flags->v = true;
            break;
        }
      }
    }
  }
}

