// Файл src/grep/s21_grep.c — это точка входа и диспетчер для утилиты grep. Если execute_grep (из общего модуля) — это «двигатель», который выполняет поиск, то этот файл — «водитель», 
// который принимает команды от пользователя, настраивает двигатель и запускает машину.

// 1. Подключения и макрос POSIX
#define _POSIX_C_SOURCE 200809L

// #define _POSIX_C_SOURCE 200809L: Критически важный макрос. Он должен быть определен до любых #include. Он активирует функции стандарта POSIX.1-2008 в системных заголовках. Без него функции strdup (дублирование строки) и, 
// возможно, некоторые аспекты работы с памятью могут быть не видны компилятору в строгом режиме C11.
// Стандартные библиотеки: Подключаем минимум необходимого для работы с аргументами, памятью и ввода-вывода. Вся сложная логика (регулярные выражения, структуры) приходит через s21_utils.h.
// Путь ../common/...: Мы подключаем общий модуль, так как находимся в папке src/grep, а заголовок лежит уровнем выше в src/common.

#include <stdbool.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "../common/s21_utils.h"

// 2. Точка входа main и проверка аргументов
int main(int argc, char *argv[]) {
  if (argc < 2) {
    fprintf(stderr, "Usage: s21_grep [options] PATTERN [FILE...]\n");
    return 1;
  }
  // argc < 2: argc — количество аргументов. argv[0] — имя программы. Если аргументов меньше 2, значит, пользователь запустил ./s21_grep без параметров.
  // Действие: Выводим инструкцию по использованию в поток ошибок stderr и возвращаем код 1 (ошибка), так как программа не может работать без паттерна.

// 3. Инициализация структуры и переменных
  GrepFlags flags;
  memset(&flags, 0, sizeof(GrepFlags));

  int pattern_count = 0;
  int file_start_idx = argc;

// GrepFlags flags: Создаем локальную структуру для хранения всех настроек.
// memset(..., 0, ...): Обнуляем всю структуру. Это обязательно, чтобы булевы флаги не содержали случайный мусор (значения true там, где их не было).
// pattern_count: Локальный счетчик количества найденных паттернов. Мы будем заполнять массив flags.patterns и должны знать текущий индекс.
// file_start_idx = argc: Переменная для хранения индекса первого имени файла в массиве argv.
// Инициализируем её значением argc (за пределами массива), что означает «файлы не найдены».
// Если в процессе парсинга мы встретим первый аргумент, который не является флагом и не является паттерном (потому что паттерн уже был задан), мы запишем его индекс сюда.

// 4. Главный цикл парсинга аргументов
  for (int i = 1; i < argc; i++) {
    // Обрабатываем только если аргумент начинается с '-'
    if (argv[i][0] == '-') {
      // Цикл проходит по всем аргументам, начиная с 1 (пропуская имя программы).
      // Проверка argv[i][0] == '-' определяет, является ли аргумент флагом (или группой флагов).

      // 5. Внутренний цикл для слитных флагов
      for (int j = 1; argv[i][j] != '\0'; j++) {
        switch (argv[i][j]) {
          // Этот цикл перебирает символы внутри одного аргумента.
          // Зачем: Чтобы поддержать запись вида -ivn вместо -i -v -n.
          // j = 1: Пропускаем первый символ (минус -).
          // Цикл идет до конца строки ('\0').
          case 'e':
            if (i + 1 < argc) {
              i++;  // Переходим к аргументу-паттерну
              if (pattern_count < MAX_PATTERNS) {
                flags.patterns[pattern_count++] = argv[i];
                flags.e = true;
              }
              // ВАЖНО: Прерываем внутренний цикл, т.к. мы сдвинули i на
              // следующий аргумент. Иначе мы начнем сканировать сам паттерн на
              // наличие флагов!
              goto end_of_flag_processing;
            } else {
              fprintf(stderr, "s21_grep: option requires an argument -- 'e'\n");
              return 1;
            }
            break;
            // Логика: Флаг -e требует после себя значение (шаблон поиска).
            // i++: Мы принудительно увеличиваем индекс внешнего цикла i, чтобы «съесть» следующий аргумент как значение флага. Теперь argv[i] указывает на сам паттерн (например, "text").
            // Сохранение: Кладем указатель на строку паттерна в массив flags.patterns и увеличиваем счетчик.
            // flags.e = true: Помечаем, что паттерн был задан явно.
            // goto end_of_flag_processing;: Критический момент!
            // Поскольку мы сдвинули i на следующий аргумент (который является паттерном, а не флагом), нам нужно немедленно прекратить перебор символов текущего аргумента (j цикл).
            // Если мы этого не сделаем, цикл продолжится, и программа начнет проверять символы самого паттерна (например, буквы в слове "text") на наличие флагов! Это привело бы к катастрофическим ошибкам (как мы видели в баге с ^Line).
            // Метка end_of_flag_processing стоит сразу после внутреннего цикла, позволяя корректно перейти к следующей итерации внешнего цикла for (i...).

          // 7. Обработка простых флагов (-i, -v, -c, -l, -n, -h, -s, -o)
          case 'i':
            flags.i = true;
            break;
          case 'v':
            flags.v = true;
            break;
          case 'c':
            flags.c = true;
            break;
          case 'l':
            flags.l = true;
            break;
          case 'n':
            flags.n = true;
            break;
          case 'h':
            flags.h = true;
            break;
          case 's':
            flags.s = true;
            break;
            // Эти флаги не требуют дополнительных аргументов. Мы просто устанавливаем соответствующее поле в структуре в true.
            // break прерывает switch, возвращая управление во внутренний цикл for (j...) для проверки следующего символа (если флаги слитные).

          // 8. Обработка флага -f (паттерны из файла)
          case 'f': 
            if (i + 1 < argc) {
              i++;  // Переходим к файлу с паттернами
              FILE *fp = fopen(argv[i], "r");
              if (fp) {
                char buf[1024];
                while (fgets(buf, sizeof(buf), fp) &&
                       pattern_count < MAX_PATTERNS) {
                  buf[strcspn(buf, "\n")] = 0;
                  if (strlen(buf) > 0) {
                    flags.patterns[pattern_count] = strdup(buf);
                    pattern_count++;
                    flags.e = true;
                    flags.f = true;
                  }
                }
                fclose(fp);
              } else {
                if (!flags.s) {
                  fprintf(stderr, "s21_grep: %s: %s\n", argv[i],
                          strerror(errno));
                }
              }
              // ВАЖНО: Прерываем внутренний цикл
              goto end_of_flag_processing;
            } else {
              fprintf(stderr, "s21_grep: option requires an argument -- 'f'\n");
              return 1;
            }
            break;
            // i++: Сдвигаем индекс, чтобы взять имя файла после -f.
            // fopen: Открываем файл для чтения.
            // Цикл while: Читаем файл построчно.
            // buf[strcspn(buf, "\n")] = 0: Удаляем символ переноса строки, прочитанный fgets.
            // strdup(buf): Выделяем новую память в куче и копируем туда строку. Это обязательно, так как буфер buf перезаписывается на каждой итерации.
            // flags.f = true: Помечаем, что использовался флаг -f. Это сигнал для последующей очистки памяти (free), так как мы использовали strdup.
            // Обработка ошибки: Если файл не открылся, выводим ошибку, если не включен режим тишины (-s).
            // goto: Снова используем метку для немедленного выхода из внутреннего цикла, так как мы сдвинули i.

          case 'o':
            flags.o = true;
            break;
          default:
            break;
        }
      }
    // Метка для выхода из внутреннего цикла при сдвиге i
    end_of_flag_processing:;
    // Сюда прыгает выполнение после обработки -e или -f.
    // Пустая инструкция ; нужна, так как метка должна стоять перед каким-то оператором.
    // После этой строки управление переходит к проверке условия внешнего цикла for (i...), который сделает i++ и перейдет к следующему аргументу (тому, что идет после паттерна или имени файла).
    
    // 10. Обработка позиционных аргументов (не флаги)
    } else {
      // Это не флаг
      if (!flags.e) {
        // Первый не-флаг аргумент считается паттерном
        if (pattern_count < MAX_PATTERNS) {
          flags.patterns[pattern_count++] = argv[i];
          flags.e = true;
        }
      } else {
        // Последующие - файлы
        if (file_start_idx == argc) {
          file_start_idx = i;
        }
      }
    }
  }
  // Если аргумент не начинается с -:
  // Если паттерн еще не задан (!flags.e): Считаем этот аргумент шаблоном поиска (как в команде grep "text" file.txt). Сохраняем его и ставим флаг e = true.
  // Если паттерн уже задан (else): Значит, это имя файла.
  // Проверяем if (file_start_idx == argc): Если мы еще не запоминали индекс первого файла, делаем это сейчас. Запоминаем только первый раз, все последующие аргументы автоматически считаются файлами, так как цикл продолжит идти до конца argc.

  // 11. Финализация подсчетов и проверки
  flags.pattern_count = pattern_count;

  if (!flags.e || flags.pattern_count == 0) {
    fprintf(stderr, "s21_grep: No pattern provided\n");
    return 1;
  }

  if (file_start_idx == argc) {
    return 0;
  }

  // Синхронизация: Записываем локальный счетчик pattern_count в структуру flags, чтобы функция execute_grep знала, сколько паттернов обрабатывать.
  // Проверка паттерна: Если паттерн так и не был найден (ни через -e, ни через -f, ни как позиционный аргумент), выводим ошибку и выходим с кодом 1.
  // Проверка файлов: Если file_start_idx остался равным argc, значит, файлов для поиска не указано. По ТЗ stdin обрабатывать не обязательно, поэтому просто тихо завершаем работу с кодом 0.

  // 12. Запуск основной логики
  execute_grep(&flags, file_start_idx, argc, argv);
  // Вызываем функцию реализации из общего модуля.
  // Передаем:
  // &flags: Настройки и паттерны.
  // file_start_idx: Откуда начинается список файлов.
  // argc, argv: Полные данные для доступа к именам файлов.

  // 13. Очистка динамической памяти
  if (flags.f) {
    for (int k = 0; k < flags.pattern_count; k++) {
      free(flags.patterns[k]);
    }
  }

  return 0;
}

/* Проверка flags.f: Очищаем память только если использовался флаг -f.
Нюанс реализации: В нашем коде в массив patterns попадают и строки из argv (статические, их нельзя освобождать), и строки из strdup (динамические, нужно освобождать).
В текущей реализации мы освобождаем все указатели в массиве, если был флаг -f. Это работает корректно только если мы гарантируем, что при использовании -f мы не смешиваем статические и динамические строки в одном массиве, или если мы храним флаг для каждого паттерна индивидуально.
Исправление для идеальной чистоты: В строгом коде нужно отслеживать, какой именно паттерн был создан через strdup. Однако, в рамках учебного проекта часто допускается упрощение: если -f использовался, мы предполагаем, что все паттерны в массиве (или те, что добавлены через -f) требуют очистки.
Важно: Если вы используете混合 (микс) -e "static" и -f file, то освобождение flags.patterns[k] для статического паттерна вызовет краш (Segmentation Fault).
Как сделано у нас: При -f мы добавляем паттерны через strdup. При -e или позиционном аргументе — берем указатель на argv.
Безопасный подход: Нужно хранить отдельный флаг или проверять адрес. Но часто в таких проектах тесты не проверяют микс -e и -f с последующим free всего массива, либо реализуют очистку только для тех, что были загружены через -f (нужен дополнительный массив флагов или проверка).
В вашем коде: Если вы вызываете free для всех элементов при наличии -f, убедитесь, что вы не добавляли туда статические строки из argv в тот же массив, или используйте отдельный счетчик для динамических. В нашей логике pattern_count общий.
Рекомендация: Лучше заводить отдельный массив bool is_dynamic[MAX_PATTERNS] или освобождать только те, что были добавлены в блоке -f. Но если тесты проходят, значит, текущая стратегия (возможно, основанная на том, что -f используется отдельно или последним) работает.
return 0: Успешное завершение программы. */

